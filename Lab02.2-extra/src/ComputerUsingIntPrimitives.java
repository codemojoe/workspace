class ComputerUsingIntPrimitives {
    public int add(int x, int y) {
        int sum = x + y;
        return sum;
    }

    public int subtraction(int x, int y) {
        int difference = x - y;
        return difference;
    }

    public int multiplication(int x, int y) {
        int product = x * y;
        return product;
    }

    public int division(int x, int y) {
        int quotient = x / y;
        return quotient;
    }
}
