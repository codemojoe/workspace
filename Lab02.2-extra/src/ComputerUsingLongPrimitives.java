class ComputerUsingLongPrimitives {
    public long add(long x, long y) {
        long sum = x + y;
        return sum;
    }

    public long subtraction(long x, long y) {
        long difference = x - y;
        return difference;
    }

    public long multiplication(long x, long y) {
        long product = x * y;
        return product;
    }

    public long division(long x, long y) {
        long quotient = x / y;
        return quotient;
    }
}
